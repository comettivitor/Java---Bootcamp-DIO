public class Operacoes {
    public static void soma(int num1, int num2){
        double resultado = num1 + num2 ;
        System.out.println("O resultado de soma é: " + resultado);
    }

    public static void subtracao(int num1, int num2){
        double resultado = num1 - num2 ;
        System.out.println("O resultado de subtração é: " + resultado);
    }

    public static void multiplicacao(int num1, int num2){
        double resultado = num1 * num2 ;
        System.out.println("O resultado de multiplicação é: " + resultado);
    }

    public static void divisao(int num1, int num2){
        double resultado = num1 / num2 ;
        System.out.println("O resultado de divisão é: " + resultado);
    }
}
